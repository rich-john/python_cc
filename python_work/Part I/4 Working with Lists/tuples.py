# A tuple uses parenthesese () instead of brackets []
# A tuple is actually defined by the presence of a comma , 
# If you want to define a tuple with a single dimension, you need to include the comma
# dimension = (200,)
# You can't modify a tuple, but you can assign a new value | tuple = (200,) tuple = (300,)

